package chunks

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestDeer(t *testing.T) {
	assert := assert.New(t)

	data := []byte{0x48, 0x49, 0x52, 0x53, 0x43, 0x48, 0x32, 0x00, 0x7D, 0x21, 0xF1, 0x77, 0xFF, 0xFF, 0xFF, 0xFF, 0x28, 0x00, 0x00, 0x00, 0x07, 0x18, 0x09, 0x00, 0x02, 0x00, 0x00, 0x00, 0x07, 0x18, 0x1D, 0x00, 0x02, 0x00, 0x00, 0x00, 0x07, 0x1E, 0x17, 0x00, 0xC2, 0xDA, 0x01, 0x00, 0x07, 0x1E, 0x13, 0x00, 0x02, 0x00, 0x00, 0x00, 0x07, 0x0A, 0x1B, 0x00, 0x02, 0x00, 0x00, 0x00}
	chunk, err := NewChunk(data)
	assert.Nil(err)
	deers, err := NewDeer(chunk)
	assert.Nil(err)
	assert.Equal(deers.Deers[0].IslandNumber, uint(7))
	assert.Equal(deers.Deers[1].IslandNumber, uint(7))
	assert.Equal(deers.Deers[2].IslandNumber, uint(7))
	assert.Equal(deers.Deers[3].IslandNumber, uint(7))
}
